package arrays;

public class DeclareAndInstantiateArray {
    public static void main(String[] args) {
        // Declare and instantiate the array with space for 4 elements
        int[] ages = new int[4];

        // Set the first element to 18
        ages[0] = 18;

        // Print the first element to verify
        System.out.println("First element (ages[0]): " + ages[0]);
    }
}
