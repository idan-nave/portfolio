public class DivisibleByFive {
    public static void main(String[] args) {
        // Loop through numbers from 1 to 100
        for (int i = 1; i <= 100; i++) {
            // Check if the number is divisible by 5
            if (i % 5 == 0) {
                System.out.println(i); // Print the number
            }
        }
    }
}

